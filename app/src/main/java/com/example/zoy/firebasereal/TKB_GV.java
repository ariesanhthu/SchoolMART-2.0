package com.example.zoy.firebasereal;

public class TKB_GV {
    private String lop;
    private String mon;
    private String buoi;

    public TKB_GV() {
    }

    public TKB_GV(String lop, String mon) {
        this.lop = lop;
        this.mon = mon;
    }

    public String getLop() {
        return lop;
    }

    public void setLop(String lop) {
        this.lop = lop;
    }

    public String getMon() {
        return mon;
    }

    public void setMon(String mon) {
        this.mon = mon;
    }

    public String getBuoi() {
        return buoi;
    }

    public void setBuoi(String buoi) {
        this.buoi = buoi;
    }
}
